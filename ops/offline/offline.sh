  -H "Content-Type: application/json" \
  -d '{"action": "create_student", "email": "student@offline.local", "password": "student123", "age": 18}')

if [ $? -eq 0 ]; then
    echo "âœ… Created student user"
else
    echo "âš ï¸  Failed to create student user via testkit, continuing..."
fi

# Create minor student with guardian
MINOR_RESPONSE=$(curl -s -X POST http://localhost:54321/functions/v1/testkit/reset \
  -H "Content-Type: application/json" \
  -d '{"action": "create_minor_with_guardian", "student_email": "minor@offline.local", "guardian_email": "guardian@offline.local", "student_password": "minor123", "guardian_password": "guardian123", "age": 16}')

if [ $? -eq 0 ]; then
    echo "âœ… Created minor student with guardian"
else
    echo "âš ï¸  Failed to create minor student via testkit, continuing..."
fi

echo ""

# Install web dependencies if needed
echo "== Checking web dependencies =="
if [ ! -d "web/node_modules" ]; then
    echo "Installing web dependencies..."
    (cd web && npm install)
else
    echo "âœ… Web dependencies already installed"
fi
echo ""

# Start Next.js in offline mode
echo "== Starting Next.js in offline mode =="
echo "ðŸš€ Starting development server..."
echo "ðŸ“± App will be available at: http://localhost:3000"
echo ""
echo "=== OFFLINE REVIEW KIT READY ==="
echo ""
echo "ðŸ”— URLs:"
echo "  â€¢ App: http://localhost:3000"
echo "  â€¢ Supabase Studio: http://localhost:54323"
echo "  â€¢ API: http://localhost:54321"
echo ""
echo "ðŸ‘¤ Test Users:"
echo "  â€¢ Admin: admin@offline.local / admin123"
echo "  â€¢ Student: student@offline.local / student123"
echo "  â€¢ Minor: minor@offline.local / minor123"
echo "  â€¢ Guardian: guardian@offline.local / guardian123"
echo ""
echo "ðŸ’¡ Tips:"
echo "  â€¢ Turn off WiFi to test true offline mode"
echo "  â€¢ Use browser dev tools to verify no external requests"
echo "  â€¢ Check console for offline mode indicators"
echo ""
